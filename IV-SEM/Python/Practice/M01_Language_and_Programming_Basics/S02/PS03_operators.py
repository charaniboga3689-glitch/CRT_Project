x = 7
y = 13
print(x & y)
print(x | y)
print(x ^ y)
print(~x)
print(x << 2)
print(x >> 2)

#Membership Operators
print(100 in [10, 20, 30, 40, 50])
print("on" in "python")

#Identity Operators ==> is and is not
x = 10
y = 20
z = 10
print(x is y)
print(x is z)

l1 = [1, 2, 3]
l2 = [1, 2, 3]
print(l1 is l2)